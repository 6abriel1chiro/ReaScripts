function Get_relevant_kb_ini_line(command)
  if kb_shortcuts_t ~= nil then
  
    -- if custom / script / sws --
    if string.sub(command, 0,1) == "_" then  
      name_to_search = string.sub(command, 2)  
      for i = 1, #kb_shortcuts_t do
        kb_shortcut = kb_shortcuts_t[i]
        start_search = string.find(kb_shortcut, name_to_search)
        if start_search ~= nil and string.sub(kb_shortcut,1,3) ~= "KEY" then kb_shortcut_ret = kb_shortcut end       
      end     
      
      --[[com_lookup = reaper.NamedCommandLookup(command)
      if com_lookup ~= 0 then
        for i = 1, #actions_table do
          action = actions_table[i]
          sub_t = {}
          for word in string.gmatch(action, "%S+") do
            table.insert(sub_t, word)
          end
          if tostring(com_lookup) == sub_t[2] 
            then action_line = action break 
          end
        end   
      end ]]
    end
    
     -- if main action 
    if string.sub(command, 0,1) ~= "_" then 
      name_to_search = command
      for i = 1, #actions_table do
        action = actions_table[i]
        sub_t = {}
        for word in string.gmatch(action, "%S+") do
          table.insert(sub_t, word)
        end
        if name_to_search == sub_t[2] then action_line = action end
        if name_to_search == sub_t[3] then action_line = action_line.."\n"..action end
      end      
    end
    
  end  
  return kb_shortcut_ret, action_line
end

