function Get_table_from_kb_ini()
  kb_shortcuts_t = {}
  path = reaper.GetExePath()
  kb_file_loc = path.."\\".."reaper-kb.ini"
  file = io.open(kb_file_loc, "r")
  content = file:read("*all")
  for line in io.lines(kb_file_loc) do table.insert(kb_shortcuts_t, line) end
  file:close()
  return kb_shortcuts_t
end

