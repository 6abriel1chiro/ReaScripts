  function Get_values_from_ini(info_line)

    if info_line ~=nil then
      st_val_t = {}
      st = 0
      for i = 1, 4 do
        st = string.find(info_line, '"', st+1)
        table.insert(st_val_t, st)
      end
      
      set_line = string.sub(info_line, 0, st_val_t[1]-2)
      set_line_t = {}
      for word in string.gmatch(set_line, "[%S_]+") do
        table.insert(set_line_t, word)
      end
      
      reapername = string.sub(info_line, st_val_t[3]+1, st_val_t[4]-1) 
      location = string.sub(info_line, st_val_t[4]+1)
      if string.sub(location, 2,3) ~= ":" then location = path.."\\"..location end
      t1, t2 = string.find(info_line, "ACT")
      if t1 == 1 then  -- if custom
        custom_t = {}
        for word in string.gmatch(location, "[%S_]+") do
          table.insert(custom_t, word)
        end
        if custom_t ~= nil then
          custom_string = ""
          if set_line_t[3] == "0" then  
            for i = 1, #custom_t do
              if string.find(custom_t[i], "_") == 1 then
                custom_string = custom_string.."reaper.Main_OnCommand(reaper.NamedCommandLookup('"..custom_t[i].."'), 0)".."\n"               
               else
                custom_string = custom_string.."reaper.Main_OnCommand("..custom_t[i]..", 0)".."\n"              
              end  
            end
          end  
          if set_line_t[3] == "32060" then 
            for i = 1, #custom_t do
              if string.find(custom_t[i], "_") == 1 then
                custom_string = custom_string.."reaper.MIDIEditor_OnCommand(reaper.NamedCommandLookup('"..custom_t[i].."'), 0)".."\n"               
               else
                custom_string = custom_string.."reaper.MIDIEditor_OnCommand("..custom_t[i]..", 0)".."\n"              
              end  
            end          
          end
        end  
        location = nil
      end
    end
    return reapername, location, custom_string
  end    
    
